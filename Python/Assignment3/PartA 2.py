#Program that finds that average of a test score

Test_Score1 = 100 #<-Enter First Test Score

Test_Score2 = 90 #<-Enter Second Test Score

Test_Score3 = 70 #<-Enter Three Test Score

num_of_tests = 3 #<- Number of grades

#Calculations

#calculate the average
average = (Test_Score1 + Test_Score2 + Test_Score3)/num_of_tests

#print the average
print(average)
